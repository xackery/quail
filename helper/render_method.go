package helper

import (
	"fmt"
	"sync"
)

var (
	mu sync.RWMutex
)

func RenderMethodStr(in uint32) string {
	mu.RLock()
	defer mu.RUnlock()
	if val, ok := methods[in]; ok {
		return val
	}
	return fmt.Sprintf("UNKNOWN_0x%x", in)
}

func RenderMethodInt(in string) uint32 {
	mu.RLock()
	defer mu.RUnlock()
	for key, val := range methods {
		if val == in {
			return key
		}
	}
	return 0
}

